const age = 15

if(age < 1){
    console.log('infant')
} else if (age >= 1 && age < 3) {
    console.log('toddler')
} else if (age >= 3 && age < 11) {
    console.log('child')
} else if (age >= 11 && age < 13) {
    console.log('preteen')
} else if (age >= 13 && age < 18) {
    console.log('teen')
} else {
    console.log('young adult')
}
